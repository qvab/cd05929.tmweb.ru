<h2>Участники Хартии</h2>

<p class="mark">Стремятся приобретать сельскохозяйственную продукцию непосредственно у сельхозпроизводителей, переработчиков, либо фирм-комиссионеров, а также у других добросовестных участников рынка</p>

<div class="content-media d-sm-block d-none">
