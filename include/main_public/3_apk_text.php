</div>

<p class="mark">Проявляют должную осмотрительность при выборе грузоперевозчика, в том числе: заключают договоры непосредственно с компаниями перевозчиками или фирмами-посредниками, которые действуют по договору комиссии непосредственно по поручению или от лица перевозчика</p>

<div class="content-media d-sm-block d-none">
    <img src="/local/templates/main_public/images/img_hands_deal.jpg">
</div>

<p class="mark">Приобретая сельскохозяйственную продукцию у поставщиков, не являющихся производителями сельхозпродукции, участники Хартии обязуются проявлять должную осмотрительность и добросовестность при выборе контрагента и стремятся приобретать сельскохозяйственную продукцию у фирм-посредников по договорам комиссии по поручению или от лица сельхозпроизводителя</p>
